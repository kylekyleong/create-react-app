import React from "react";
// Error boundaries: catch JavaScript errors anywhere in their child component tree, log those errors, and display a fallback UI
// Error boundaries do not catch erros for
// - Event handlers
// - Asynchronous code
// - Server side rendering
// - Errors thrown in the error boundaries iteself

class ErrorBoundary extends React.Component {
  constructor(props) {
    super(props);
    this.state = { hasError: false };
  }

  static getDerivedStateFromError(error) {
    // Update state so the next render will show the fallback UI.
    return { hasError: true };
  }

  componentDidCatch(error, errorInfo) {
    // You can also log the error to an error reporting service
    console.log(error, errorInfo);
  }

  render() {
    if (this.state.hasError) {
      // You can render any custom fallback UI
      return <h1>Something went wrong.</h1>;
    }
    return this.props.children;
  }
}

export default ErrorBoundary;
